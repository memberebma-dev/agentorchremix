interface WebsitePreviewData {
  heroHeadline: string;
  heroSubheadline: string;
  trustBadges: string[];
  pillars: { title: string; description: string }[];
  cta: string;
}

function isWebsiteData(v: any): v is WebsitePreviewData {
  return v && typeof v === "object" && Array.isArray(v.pillars);
}

