  const leadId = c.req.param("leadId");
  const leads = (await blink.db.leads.list({ where: { id: leadId }, limit: 1 })) as any[];
  const lead = leads[0];
  const assets = (await blink.db.generatedAssets.list({ where: { leadId, type: "custom_website" }, limit: 1 })) as any[];
  const asset = assets[0];
  if (!asset) return c.text("Website preview not found for this lead.", 404);
  const companyName = lead?.companyName || "Preview";

  let parsed: any = null;
