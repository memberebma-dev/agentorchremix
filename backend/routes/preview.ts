const previewApp = new Hono<{ Bindings: Record<string, string> }>();

function escapeHtml(s: string): string {
  return s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
}

const BASE_STYLE = `
  * { box-sizing: border-box; }
  body { font-family: -apple-system, BlinkMacSystemFont, "Segoe UI", Roboto, Arial, sans-serif; margin: 0; color: #1e293b; background: #f8fafc; line-height: 1.65; }
  .footer { text-align: center; padding: 24px 20px; color: #94a3b8; font-size: 12px; }
`;

function wrapPage(title: string, bodyHtml: string, extraStyle = ""): string {
  return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8" />
<meta name="viewport" content="width=device-width, initial-scale=1.0" />
<title>${escapeHtml(title)}</title>
<style>${BASE_STYLE}${extraStyle}</style>
</head>
<body>
${bodyHtml}
<div class="footer">Generated by AgentOrch</div>
</body>
</html>`;
}

interface AuditFinding {
  title: string;
  severity: "critical" | "high" | "medium";
  description: string;
  fix: string;
  impact: string;
}

interface AuditReportData {
  executiveSummary: string;
  findings: AuditFinding[];
  quickWins: string[];
  projectedImpact: string;
  nextSteps: string;
}

function isAuditData(v: any): v is AuditReportData {
  return v && typeof v === "object" && Array.isArray(v.findings);
}

const SEVERITY_LABEL: Record<string, string> = { critical: "Critical", high: "High Priority", medium: "Medium" };

