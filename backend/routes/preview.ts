function renderLegacyText(title: string, content: string): string {
  return wrapPage(
    title,
    `<div class="wrap"><h1>${escapeHtml(title)}</h1><pre>${escapeHtml(content)}</pre></div>`,
    `.wrap { max-width: 720px; margin: 0 auto; padding: 56px 24px 24px; } h1 { color: #0D9488; } pre { white-space: pre-wrap; font-family: inherit; background: #fff; border: 1px solid #e2e8f0; border-radius: 14px; padding: 24px; }`
  );
}

previewApp.get("/audit/:leadId", async (c) => {
  const env = c.env as Record<string, string>;
  const blink = createClient({ projectId: env.BLINK_PROJECT_ID, secretKey: env.BLINK_SECRET_KEY });
  const leadId = c.req.param("leadId");
  const leads = (await blink.db.leads.list({ where: { id: leadId }, limit: 1 })) as any[];
  const lead = leads[0];
  const assets = (await blink.db.generatedAssets.list({ where: { leadId, type: "audit_report" }, limit: 1 })) as any[];
  const asset = assets[0];
  if (!asset) return c.text("Audit report not found for this lead.", 404);
  const companyName = lead?.companyName || "Report";

  let parsed: any = null;
  try { parsed = JSON.parse(asset.content || ""); } catch { /* legacy plain-text asset */ }

  const html = isAuditData(parsed)
    ? renderAuditReport(companyName, parsed)
    : renderLegacyText(`SEO Audit — ${companyName}`, asset.content || "");
  return c.html(html);
});

